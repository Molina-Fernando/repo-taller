package clases;

/**
 * La enumeración Lugares representa los posibles lugares o áreas en un entorno
 * médico, estos lugares pueden incluir consultorios, áreas de emergencia e
 * internación, entre otros.
 */
public enum Lugares {
    /**
     * Consultorio médico donde se realizan consultas y exámenes médicos.
     */
    CONSULTORIO,
    /**
     * Área de emergencia donde se atienden casos de urgencia y emergencia
     * médica.
     */
    EMERGENCIA,
    /**
     * Área de internación o boxes del hospital donde los pacientes son
     * hospitalizados.
     */
    INTERNACION;
}
